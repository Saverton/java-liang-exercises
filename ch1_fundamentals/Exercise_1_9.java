/**
 * (Area and perimeter of a Rectangle) Write a program that displays the area of a rectangle with a width of 4.5 and a height of 7.9.
 * @author Scott M.
 */

public class Exercise_1_9 {
    /** Main method to run program. */
    public static void main(String[] args) {
       System.out.println("Width = " + 4.5);
       System.out.println("Height = " + 7.9);
       System.out.println("Area = " + (4.5 * 7.9));
       System.out.println("Perimeter = " + (2 * 4.5 + 2 * 7.9)); 
    }
}
