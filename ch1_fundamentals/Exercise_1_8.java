/**
 * (Area and perimeter of a circle) Write a program that prints the area and perimeter of a circle with radius 5.5.
 * @author Scott M.
 */
public class Exercise_1_8 {
    /** Main method to run program. */
    public static void main(String[] args) {
        System.out.println("Radius = " + 5.5);
        System.out.println("Area = " + ((5.5 * 5.5) * 3.14));
        System.out.println("Perimeter = " + (2 * 3.14 * 5.5));
    }
}
