/**
 * (Print a Table) Write a program that displays a table with: (columns) a, a^2, a^3, (rows) range 1-4.
 * @author Scott M.
 */
public class Exercise_1_4 {
    /** Main method to run program. */
    public static void main(String[] args) {
        System.out.println("a\ta^2\ta^3\t");
        System.out.println((1) + "\t" + (1 * 1) + "\t" + (1 * 1 * 1));
        System.out.println((2) + "\t" + (2 * 2) + "\t" + (2 * 2 * 2));
        System.out.println((3) + "\t" + (3 * 3) + "\t" + (3 * 3 * 3));
        System.out.println((4) + "\t" + (4 * 4) + "\t" + (4 * 4 * 4));
    }
}
